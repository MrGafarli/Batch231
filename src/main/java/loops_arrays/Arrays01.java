package loops_arrays;

import java.util.Arrays;

public class Arrays01 {
    public static void main(String args[]) {

        int[] say = new int[4];
        System.out.println(Arrays.toString(say));
        say[0] = 4;
        say[1] = 7;
        say[2] = 16;
        say[3] = 93;
        System.out.println(say[3]);
        System.out.println(Arrays.toString(say));

    }
}
