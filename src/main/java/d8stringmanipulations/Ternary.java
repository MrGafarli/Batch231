package d8stringmanipulations;

public class Ternary {
    public static void main(String[] args) {
        int yil = 1;
        switch (yil){
            case 1:
                System.out.println("1ci il");
                break;
            case 2:
                System.out.println("2ci il");
                break;
            case 3:
                System.out.println("3cu il");
            default:
                System.out.println("4cu il");
        }
    }
}
